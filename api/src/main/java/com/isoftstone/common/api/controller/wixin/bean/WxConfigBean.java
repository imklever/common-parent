package com.isoftstone.common.api.controller.wixin.bean;

public class WxConfigBean {
    private String app_id;
    private String secret;
    private String token;
    private String aes_key;
    private String msg_data_format;
    private String mchid;
    private String sign_key;
    private String notify_url;
    private String ip;
    private String desc;
    public String getApp_id() {
        return app_id;
    }
    public void setApp_id(String app_id) {
        this.app_id = app_id;
    }
    public String getSecret() {
        return secret;
    }
    public void setSecret(String secret) {
        this.secret = secret;
    }
    public String getToken() {
        return token;
    }
    public void setToken(String token) {
        this.token = token;
    }
    public String getAes_key() {
        return aes_key;
    }
    public void setAes_key(String aes_key) {
        this.aes_key = aes_key;
    }
    public String getMsg_data_format() {
        return msg_data_format;
    }
    public void setMsg_data_format(String msg_data_format) {
        this.msg_data_format = msg_data_format;
    }
    public String getMchid() {
        return mchid;
    }
    public void setMchid(String mchid) {
        this.mchid = mchid;
    }
    public String getSign_key() {
        return sign_key;
    }
    public void setSign_key(String sign_key) {
        this.sign_key = sign_key;
    }
    public String getNotify_url() {
        return notify_url;
    }
    public void setNotify_url(String notify_url) {
        this.notify_url = notify_url;
    }
    public String getIp() {
        return ip;
    }
    public void setIp(String ip) {
        this.ip = ip;
    }
    public String getDesc() {
        return desc;
    }
    public void setDesc(String desc) {
        this.desc = desc;
    }
    
    
}
